=begin

Input: collection (and block)
Output: boolean true/false
- returns true if all elements processed in block return truthy
- `#all?` stops processing first time block returns false
- empty array should return true

Examples:
all?([1, 3, 5, 6]) { |value| value.odd? } == false
all?([1, 3, 5, 7]) { |value| value.odd? } == true
all?([2, 4, 6, 8]) { |value| value.even? } == true
all?([1, 3, 5, 7]) { |value| value % 5 == 0 } == false
all?([1, 3, 5, 7]) { |value| true } == true
all?([1, 3, 5, 7]) { |value| false } == false
all?([]) { |value| false } == true

Data Structures:
Array input
Block input
Boolean output

Algorithm:
Iterate through input array. Each element...
- pass element into block
- return false if block returns false
Return true

=end

def all?(collection)
  collection.each { |ele| return false unless yield(ele) }
  true
end

p all?([1, 3, 5, 6]) { |value| value.odd? } == false
p all?([1, 3, 5, 7]) { |value| value.odd? } == true
p all?([2, 4, 6, 8]) { |value| value.even? } == true
p all?([1, 3, 5, 7]) { |value| value % 5 == 0 } == false
p all?([1, 3, 5, 7]) { |value| true } == true
p all?([1, 3, 5, 7]) { |value| false } == false
p all?([]) { |value| false } == true
